    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
    Product p = new Product();
    p.setName(txtName.getText());
    p.setCategory(cbCategory.getSelectedItem().toString());
    p.setPrice(Double.parseDouble(txtPrice.getText()));
    p.setStatus(cbStatus.getSelectedItem().toString());

    if (productService.addProduct(p)) {
        loadTable();
        clearForm();
        JOptionPane.showMessageDialog(this, "Thêm thành công");
    } else {
        JOptionPane.showMessageDialog(this, "Thêm thất bại");
    }
    // TODO add your handling code here:
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
    int row = tblProduct.getSelectedRow();
    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Chọn sản phẩm cần sửa");
        return;
    }
    
    Product p = new Product();
    p.setId(Integer.parseInt(txtId.getText()));
    p.setName(txtName.getText());
    p.setCategory(cbCategory.getSelectedItem().toString());
    p.setPrice(Double.parseDouble(txtPrice.getText()));
    p.setStatus(cbStatus.getSelectedItem().toString());

    if (productService.updateProduct(p)) {
        loadTable();
        clearForm();
        JOptionPane.showMessageDialog(this, "Cập nhật thành công");
    } else {
        JOptionPane.showMessageDialog(this, "Cập nhật thất bại"); 
    }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
    int row = tblProduct.getSelectedRow();
    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Chọn sản phẩm cần xóa");
        return;
    }

    int id = Integer.parseInt(txtId.getText());
    if (productService.deleteProduct(id)) {
        loadTable();
        clearForm();
        JOptionPane.showMessageDialog(this, "Xóa thành công");    
    }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void tblProductMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProductMouseClicked
    int row = tblProduct.getSelectedRow();

    txtId.setText(tableModel.getValueAt(row, 0).toString());
    txtName.setText(tableModel.getValueAt(row, 1).toString());
    cbCategory.setSelectedItem(tableModel.getValueAt(row, 2));
    txtPrice.setText(tableModel.getValueAt(row, 3).toString());
    cbStatus.setSelectedItem(tableModel.getValueAt(row, 4));
        // TODO add your handling code here:
    }//GEN-LAST:event_tblProductMouseClicked

