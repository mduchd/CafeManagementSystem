    private void loadTable() {
        List<Product> list = productService.getAllProducts();
        tableModel.setRowCount(0);

        for (Product p : list) {
            tableModel.addRow(new Object[]{
                p.getId(),
                p.getName(),
                p.getCategory(),
                p.getPrice(),
                p.getStatus()
            });
        }
    }
